package org.egov.mobileapp.models;

import java.math.BigDecimal;

public class AggregateReportPOC {
	private String receiptTypeDescription;
	private Integer numberOfReceipt;
	private BigDecimal amount;

	/**
	 * @return the receiptTypeDescription
	 */
	public String getReceiptTypeDescription() {
		return receiptTypeDescription;
	}

	/**
	 * @param receiptTypeDescription
	 *            the receiptTypeDescription to set
	 */
	public void setReceiptTypeDescription(String receiptTypeDescription) {
		this.receiptTypeDescription = receiptTypeDescription;
	}

	/**
	 * @return the numberOfReceipt
	 */
	public Integer getNumberOfReceipt() {
		return numberOfReceipt;
	}

	/**
	 * @param numberOfReceipt
	 *            the numberOfReceipt to set
	 */
	public void setNumberOfReceipt(Integer numberOfReceipt) {
		this.numberOfReceipt = numberOfReceipt;
	}

	/**
	 * @return the amount
	 */
	public BigDecimal getAmount() {
		return amount;
	}

	/**
	 * @param amount
	 *            the amount to set
	 */
	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

}
